<p style="font-family: 'Josefin Slab', serif; font-size: 25px; font-weight: bold;">What is it ?</p>
<p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
A wedding anniversary is the anniversary of the date a marriage took place.
</br></br>

Traditional names exist for all of them: for instance, 50 years of marriage is called a "golden wedding anniversary" or simply a "golden anniversary".

</p>

</br></br>

<p style="font-family: 'Josefin Slab', serif; font-size: 25px; font-weight: bold;">History and Origins</p>
<p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
The historic origins of wedding anniversaries date back to the Holy Roman Empire, where husbands crowned their wives with a silver wreath on their twenty-fifth anniversary, and a gold wreath on the fiftieth. Later, principally in the twentieth century, commercialism led to the addition of more anniversaries being represented by a named gift.
</p>

</br></br>

<p style="font-family: 'Josefin Slab', serif; font-size: 25px; font-weight: bold;">Celebration and Gifts</p>
<p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
The names of some anniversaries provide guidance for appropriate or traditional gifts for the spouses to give each other; if there is a party these can be brought by the guests or influence the theme or decoration. These gifts vary in different countries, but some years have well-established connections now common to most nations: 5th Wood, 10th Tin, 15th Crystal, 20th China, 25th Silver, 30th Pearl, 40th Ruby, 50th Gold, 60th Diamond, 70th Platinum. 
</br></br>

In English speaking countries the first, wooden, gift was cut on the day of celebration and then presented to the wife as a finished article before the next two quarter days had passed. The tradition may have originated in medieval Germany where, if a married couple lived to celebrate the 25th anniversary of their wedding, the wife was presented by her friends and neighbors with a silver wreath to congratulate them for the good fortune that had prolonged the lives of the couple. Over time the number of symbols expanded and the German tradition came to assign gifts that had direct connections with each stage of married life. The symbols have changed over time. 
</br></br>

For example, in the United Kingdom, diamond was a well known symbol for the 75th anniversary, but this changed to the now more common 60th anniversary after the Diamond Anniversary of Queen Victoria. The current monarch Queen Elizabeth II's 60th year on the throne was widely marked as her Diamond Jubilee and commemorated in 2012..
</p>
