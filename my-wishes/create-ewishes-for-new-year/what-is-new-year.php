<p style="font-family: 'Josefin Slab', serif; font-size: 25px; font-weight: bold;">What is it ?</p>
<p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
New Year is the time at which a new calendar year begins and the calendar's year count increments by one.
</br></br>

Many cultures celebrate the event in some manner and the 1st day of January is often marked as a holiday.
</br></br>

In the Gregorian calendar, the most widely used calendar system today, New Year occurs on 1 January (New Year's Day). This was also the case both in the old Roman calendar (at least after about 713 BCE) and in the Julian calendar that succeeded it.
</br></br>

Other calendars have been used historically in different parts of the world; some calendars count years numerically, while others do not.
</br></br>

During the Middle Ages in western Europe, while the Julian calendar was still in use, authorities moved New Year's Day variously, depending upon locale, to one of several other days, among them: 1 March, 25 March, Easter, 1 September, and 25 December. Beginning in 1582, the adoptions of the Gregorian calendar and changes to the Old Style and New Style dates meant the various local dates for New Year's Day changed to using one fixed date, January 1.
</br></br>

The widespread official adoption of the Gregorian calendar and marking January 1 as the beginning of a new year is almost global now. Regional or local use of other calendars continue, along with the cultural and religious practices that accompany them. In Latin America, various native cultures continue the observation of traditions according to their own calendars. Israel, China, India and other countries, continue to celebrate New Year on different dates.
</br></br>

In pre-Christian Rome under the Julian calendar, the day was dedicated to Janus, god of gateways and beginnings, for whom January is also named. As a date in the Gregorian calendar of Christendom, New Year's Day liturgically marked the Feast of the Naming and Circumcision of Jesus, which is still observed as such in the Anglican Church and Lutheran Church.
</br></br>

In present day, with most countries now using the Gregorian calendar as their de facto calendar, New Year's Day is probably the most celebrated public holiday, often observed with fireworks at the stroke of midnight as the new year starts in each time zone. Other global New Year's Day traditions include making New Year's resolutions and calling one's friends and family.
</p>
