<p style="font-family: 'Josefin Slab', serif; font-size: 25px; font-weight: bold;">What is it ?</p>
<p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
A birthday is an occasion when a person or institution celebrates the anniversary of their birth. Birthdays are celebrated in numerous cultures, often with gifts, cards, a party, or a rite of passage.
</br></br>

Many religions celebrate the birth of their founders with special holidays (e.g. Christmas, Buddha's Birthday).
</br></br>

There is a distinction between birthday and birthdate: The former, other than February 29, occurs each year (e.g. January 15), while the latter is the exact date a person was born (e.g., January 15, 2001).
</p>

