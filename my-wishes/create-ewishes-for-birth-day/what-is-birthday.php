<p style="font-family: 'Josefin Slab', serif; font-size: 25px; font-weight: bold;">Traditions</p>

<p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
In many parts of the world[vague] an individual's birthday is celebrated by a party where a specially made cake, usually decorated with lettering and the person's age, is presented. The cake is traditionally studded with the same number of lit candles as the age of the individual, or a number candle representing their age. The celebrated individual will usually make a silent wish and attempt to blow out the candles in one breath; if successful, a tradition holds that the wish will be granted. In many cultures, the wish must be kept secret or it won't "come true". Presents are bestowed on the individual by the guests appropriate to her/his age. Other birthday activities may include entertainment (sometimes by a hired professional, i.e. a clown, magician, or musician), and a special toast or speech by the birthday celebrant. The last stanza of Patty Hill's and Mildred Hill's famous song, "Good Morning to You" (unofficially titled "Happy Birthday to You") is typically sung by the guests at some point in the proceedings. In some countries a pi√±ata takes the place of a cake.
