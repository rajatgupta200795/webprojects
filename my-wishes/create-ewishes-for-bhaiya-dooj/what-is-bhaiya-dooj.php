<p style="font-family: 'Josefin Slab', serif; font-size: 25px; font-weight: bold;">What is it ?</p>
<p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
    Bhai Dooj(भाई दूज) / Bhau-Beej / Bhai Tika / Bhai Phonta  is a festival celebrated by Hindus of India and Nepal on the second lunar day of Shukla Paksha (bright fortnight) in the Vikram Samvat Hindu calendar month of Kartika. It is celebrated during the Diwali or Tihar festival.

The celebrations of this day are similar to the festival of Raksha Bandhan. On this day, sisters get gifts from brothers.

In the southern part of the country, the day is celebrated as Yama Dwitiya.
</p>
</br></br>

<p style="font-family: 'Josefin Slab', serif; font-size: 25px; font-weight: bold;">Regional names</p>

<p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
    The festival is known as:
<ul>
<li><p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
<b>Bhai Dooj (Hindi:भाई दूज)</b> in entire Northern part of India, observed during the Diwali festival. This is also the second day of the Vikrami Samvat New Year, the calendar followed in Northern India (including Kashmir), which starts from the lunar month of Kārtika. It is widely celebrated by Awadhis in Uttar Pradesh, Maithils in Bihar and people from various other ethnic groups. The first day of this New Year is observed as Govardhan Pūja.</li></br> </p>

<li><p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
     <b>Bhai Tika (Nepali:भाई टीका)</b> in Nepal, where it is the most important festival after Dashain (Vijaya Dashmi/Dussehra). Observed on the third day from Tyohar festival, it is widely celebrated by Newari, Tharu, Bahun and Chhetri people.</li></br></p>
<li><p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
<b>Bhai Phonta (Bengali:ভাই ফোঁটা)</b> in Bengal and it takes place every year on the first or the second day of the Kali Puja festival.</li></br></p>
<li><p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
<b>Bhai Bij, Bhau Beej, or Bhav Bij (Marathi : भाऊबीज) </b>amongst the Gujarati, Marathi and Konkani-speaking communities in the states of Gujarat, Maharashtra, Goa and Karnataka.</li></br></p>
<li><p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">Another name for the day is <b>Yamadwitheya or Yamadvitiya </b>, after a legendary meeting between Yama the god of Death and his sister Yamuna (the famous river) on Dwitheya (the second day after new moon).</li></ul></p>

   
</br></br>
<p style="font-family: 'Josefin Slab', serif; font-size: 25px; font-weight: bold;">The ceremony</p>
<p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
On the day of the festival, sisters invite their brothers for a sumptuous meal often including their favorite dishes/sweets. The procedure may be different in bihar and central india. The whole ceremony signifies the duty of a brother to protect his sister, as well as a sister's blessings for her brother.</br>

Carrying forward the ceremony in traditional style, sisters perform aarti for their brother and apply a red tika on the brother's forehead. This tika ceremony on the occasion of Bhai Bij signifies the sister's sincerest prayers for the long and happy life of her brother and treat them with gifts. In return brothers bless their sisters and may treat them also with gifts or cash. </br>

As it is customary in Haryana, Maharashtra to celebrate the auspicious occasion of Bhau-beej, women who do not have a brother worship the moon god instead. They apply mehendi on girls as their tradition. </br>

The sister whose brother lives far away from her and can not go to her house, sends her sincerest prayers for the long and happy life of her brother through the moon god. She performs aarti for the moon. This is the reason why children of Hindu parents affectionately call the moon Chandamama (Chanda means moon and mama means mother's brother). 
    </br></br>
    <p style="font-family: 'Josefin Slab', serif; font-size: 25px; font-weight: bold;">The celebration</p>
<p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
   Bhai Phonta in West Bengal is celebrated with much splendor. The ceremony is marked with many rituals along with a grand feast arranged for the brothers.It is necessary that, both brother and sister are more than 5 years of age.</br>

The festival of Bhai Bij is popular in Haryana, Gujarat, Maharashtra and Goa and is celebrated with great fervour and gaiety. Brothers and sisters look forward to the occasion with immense enthusiasm. To add charm to the occasion, Bhai Bij gifts are exchanged between brothers and sisters as a token of love and appreciation.</br>

Bhav Bij is a time for family reunions as all brothers and sisters in the family get together. Close relatives and friends are also invited to celebrate the Bhav Bij in many families. </br>

Special dishes for the festival include the Maharashtra sweet called basundi poori or kheerni poori.On this occasion sisters give gifts to their brothers. 
