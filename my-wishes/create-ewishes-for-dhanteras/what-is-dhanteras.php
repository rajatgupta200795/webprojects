<p style="font-family: 'Josefin Slab', serif; font-size: 25px; font-weight: bold;">What is it ?</p>
<p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
Dhana Trayodashi (or Dhanteras; Nepali: धनतेरष, Hindi: धनतेरस, Marathi: धनत्रयोदशी) is the first day of the Indian Diwali and Nepalese Tihar Festival. The festival is known as "Dhanatrayodashi" or "Dhanvantari Trayodashi". It is celebrated on the thirteenth lunar day of Krishna Paksha (dark fortnight) in the Vikram Samvat Hindu calendar month of Ashwin.
</br></br>

Dhanvantari is worshipped on the occasion of Dhanvantari Trayodashi. Dhanvantari is considered to be the teacher of all physicians and the originator of Ayurveda.</p>

</br></br>


<p style="font-family: 'Josefin Slab', serif; font-size: 25px; font-weight: bold;">Significance</p>
<p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
On the day of Dhantrayodashi Goddess Lakshmi came out from the ocean of milk during the churning of the Sea. Hence, Goddess Lakshmi, along with Lord Kuber is worshiped on the day of Trayodashi.</p>
</br></br>


<p style="font-family: 'Josefin Slab', serif; font-size: 25px; font-weight: bold;">Legends</p>
<p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
An ancient legend ascribes the occasion to an interesting story about the 16-year-old son of King Hima. His horoscope predicted his death by snake-bite on the fourth day of his marriage. On that particular day, his newly-wed wife did not allow him to sleep. She laid out all her ornaments and lots of gold and silver coins in a heap at the entrance of the sleeping chamber and lit lamps all over the place. Then she narrated stories and sang songs to keep her husband from falling asleep. The next day, when Yama, the god of Death arrived at the prince's doorstep in the guise of a Serpent, his eyes were dazzled and blinded by the brilliance of the lamps and the jewellery. Yama could not enter the Prince's chamber, so he climbed on top of the heap of gold coins and sat there the entire night listening to the stories and songs. In the morning, he silently went away. Thus, the young prince was saved from the clutches of death by the cleverness of his new bride, and the day came to be celebrated as Dhanteras. The following day came to be called Naraka Chaturdashi ('Naraka' means hell and Chaturdashi means 14th). It is also known as ‘Yamadeepdaan’ as the ladies of the house light earthen lamps or ‘deep’ and these are kept burning throughout the night glorifying Yama, the God of Death. Since this is the night before Diwali, it is also called 'Chhoti Diwali' or Minor Diwali.
</br></br>

According to another popular legend, when the Gods and demons churned the ocean for Amrita or nectar, Dhanvantari (the physician of the Gods and an incarnation of Vishnu) emerged carrying a jar of the elixir on the day of Dhanteras.</p>
</br></br>

<p style="font-family: 'Josefin Slab', serif; font-size: 25px; font-weight: bold;">Preparation</p>
<p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
On the day of Dhanteras, business premises are renovated and decorated. Entrances are made colorful with lanterns and traditional motifs of Rangoli designs to welcome the Goddess of Wealth and Prosperity. To indicate her long-awaited arrival, small footprints are drawn with rice flour and vermilion powder all over the houses. Lamps are kept burning all through the night.</p>
</br></br>

<p style="font-family: 'Josefin Slab', serif; font-size: 25px; font-weight: bold;">Traditions</p>
<p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
On Dhanteras Hindus consider it auspicious to purchase gold or silver articles or at least one or two new utensils. It is believed that new "Dhan" or some form of precious metal is a sign of good luck. "Lakshmi Puja" is performed in the evenings when tiny Diyas of clay are lit to drive away the shadows of evil spirits. "Bhajans", devotional songs in praise of Goddess Lakshmi, are also sung.</p>
</br></br>


<p style="font-family: 'Josefin Slab', serif; font-size: 25px; font-weight: bold;">Celebrations</p>
<p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
Dhanteras is celebrated with gusto and enthusiasm. "Lakshmi Puja" is performed in the evenings when tiny diyas of clay are lit to drive away the shadows of evil spirits. Bhajans, devotional songs in praise of Goddess Laxmi, are sung and "Naivedya" of traditional sweets is offered to the Goddess. There is a peculiar custom in Maharashtra to lightly pound dry coriander seeds (Dhane in Marathi for Dhanatrayodashi) with jaggery and offer as Naivedya.
</br></br>
In villages, cattle are adorned and worshiped by farmers as they form the main source of their income.
