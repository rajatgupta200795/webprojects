<p style="font-family: 'Josefin Slab', serif; font-size: 25px; font-weight: bold;">What is it ?</p>
<p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
<b>Narak Chaudas </b> (also known as Kali Chaudas, Roop Chaudas, Choti Diwali ,Naraka Chaturdashi or Naraka Nivaran Chaturdashi) is a Hindu festival, which falls on Chaturdashi (14th day) of the Krishna Paksha in the Vikram Samvat Hindu calendar month of Kartik. It is the second day of the five-day-long festival of Diwali. The Hindu literature narrates that the asura (demon) Narakasura was killed on this day by Krishna, Satyabhama and Kali. The day is celebrated by early morning religious rituals and festivities. </br></br>

<p style="font-family: 'Josefin Slab', serif; font-size: 25px; font-weight: bold;">Meaning in Hinduism</p>
<p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
    The festival is also called as "Kali Chaudas", where Kali means dark (eternal) and Chaudas means fourteenth, this is celebrated on the 14th day of the dark half of Aaso. In some regions of India, Kali Chaudas is the day allotted to the worship of Mahakali or Shakti and is believed that on this day Kali killed the asura (demon) Narakasura. Hence also referred to as Naraka-Chaturdashi, Kali Chaudas is day to abolish laziness and evil which create hell in our life and shine light on life.
</br></br>


<p style="font-family: 'Josefin Slab', serif; font-size: 25px; font-weight: bold;">Rituals associated</p>
<p style="font-family: 'PT Sans Caption', sans-serif; font-size: 18px;">
  The puja is performed with oil, flowers, and sandalwood. Coconuts are also offered to Hanuman and Prasad of sesame seed, jaggery and rice flakes (poha) with ghee and sugar.
</br></br>

The rituals of Kali Choudas is strongly suggestive of the origin of Diwali as a harvest festival is performed. On this day delicacies are prepared from pounded semi-cooked rice (called Poha or Pova). This rice is taken from the fresh harvest available at that time. This custom is prevalent both in rural and urban areas especially in Western India.</br>

</br></br>

On this day, a head wash and application of kajal in the eyes is believed to keep away the kali nazar (evil eye). Some say that those who are into tantra, learn their 'mantras' on this day. Alternatively, people offer Nivet is local to where they are originally from. This goddess is called their Kul Devi, in order to cast off evil spirits. Some families also offer food to their forefathers on this day. The second day of Diwali is known as Kali Choudas in Gujarat, Rajasthan & few part of Maharashtra.</br>
</br></br>

On this day Hindus get up earlier than usual. The men will rub their bodies in perfumed oils before bathing. Afterwards, clean clothes are worn; some people wear new ones. A large breakfast is enjoyed with relatives and friends. In the evening, a mix of bright and loud fireworks are set off in an atmosphere of joyful fun and noise. Special sweet dishes are served as part of the midday meal. House are lit with oil lamps during the evening.  
